		<h1>Users Database System - Help</h1>
		<h3>Using the online user database system is easy.</h3>
		<p>The system supports the 4 main operations of the C.R.U.D. model:</p>
		<ul>
			<li><strong>C</strong>reate</li>
			<li><strong>R</strong>ead</li>
			<li><strong>U</strong>pdate</li>
			<li><strong>D</strong>elete</li>
		</ul>
		<p>In addition, the system supports searching for any record in the database using any attribute.</p>

		<br/>
		<h3>Viewing Records</h3>
		<p>Records can be viewed by going to the page: <strong>user_list.php</strong>. You will see the records displayed in a table with the names of the columns along the top.</p>
		<h3>Adding Records</h3>
		<p>Records can be added by clicking the "Add User" button on the user listing page.</p>
		<p>You will be taken to a page where you can fill out the information for a new record.</p>
		<h3>Updating Records</h3>
		<p>A record can be updated by clicking on the "Edit" button to the right of the record you would like to edit on the user listing page.</p>
		<p>You will be taken to a screen where the current information will be filled in, and can be modified.</p>
		<p>Don't forget to click the "Save Changes" button to commit any changes to the database.</p>
		<h3>Deleting Records</h3>
		<p>Records can be deleted by clicking the "Edit" button to the right of the record you want to delete.</p>
		<p>At the bottom of the edit user screen, there will be a button to delete the record.</p>
		<h3>Searching For Records</h3>
		<p>You can search for records in the system easily. At the top of the user listing screen there is a search box, a dropdown list and a search button.</p>
		<ol>
			<li>Enter your search query in the box (case insensitive)</li>
			<li>Select the attribute by which you would like to search</li>
			<li>Click the search button</li>
		</ol>
